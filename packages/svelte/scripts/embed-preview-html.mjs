#!/usr/bin/env node
/**
 * Copies the preview HTML asset from @formepdf/renderer into this
 * package as a generated TS module (ADR 0004: the preview helper
 * copies the asset instead of depending on renderer, which would drag
 * react and esbuild into every Svelte project).
 *
 * Embedding as a module instead of shipping the raw .html means the
 * preview subpath needs no filesystem access at runtime, so it keeps
 * working when a SvelteKit production build bundles the server code.
 *
 * Runs before build, check, and test. Fails loudly when the asset is
 * missing so a broken monorepo build order can't produce a package
 * with a silently absent preview.
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const source = resolve(here, '../../renderer/src/preview/index.html');
const target = resolve(here, '../src/preview/preview-html.generated.ts');

let html;
try {
  html = readFileSync(source, 'utf-8');
} catch (cause) {
  console.error(
    `[@formepdf/svelte] preview HTML asset missing: ${source}\n` +
      `The preview helper copies it from @formepdf/renderer at build time. ` +
      `Build in monorepo order (shared -> react -> core -> renderer -> svelte) ` +
      `so packages/renderer/src/preview/index.html exists.`,
  );
  console.error(String(cause));
  process.exit(1);
}

const banner =
  '/**\n' +
  ' * Auto-generated by scripts/embed-preview-html.mjs - do not edit.\n' +
  ' * Source: packages/renderer/src/preview/index.html\n' +
  ' */\n';

mkdirSync(dirname(target), { recursive: true });
writeFileSync(
  target,
  `${banner}export const PREVIEW_HTML: string = ${JSON.stringify(html)};\n`,
  'utf-8',
);
